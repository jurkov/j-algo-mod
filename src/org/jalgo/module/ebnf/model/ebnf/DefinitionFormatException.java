package org.jalgo.module.ebnf.model.ebnf;

public class DefinitionFormatException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DefinitionFormatException() {
		super();
	}

	public DefinitionFormatException(String message) {
		super(message);
	}

	public DefinitionFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public DefinitionFormatException(Throwable cause) {
		super(cause);
	}

}

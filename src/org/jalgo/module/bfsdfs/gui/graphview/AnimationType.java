package org.jalgo.module.bfsdfs.gui.graphview;

/**
 * This enumeration represents the current animation type of a
 * <code>VisualGraphElement</code>.
 * @author Anselm Schmidt
 */
public enum AnimationType {
	/**
	 *  Creation animation.
	 */
	CREATE,
	
	/**
	 *  Focus animation.
	 */
	FOCUS,
	
	/**
	 *  Unfocus animation.
	 */
	UNFOCUS,
	
	/**
	 * Delete animation.
	 */
	DELETE,
	
	/**
	 *  No animation.
	 */
	NONE,
}

